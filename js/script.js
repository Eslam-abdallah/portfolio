// Toggle Nav
$('#open-nav').click(function(){
    $('#sidenav').toggleClass("sideshow");
    $('#logo').toggle();
    $('#open-nav').toggle();
    $('.mainx').toggle();
});
//Change Icon
$('#close-nav').click(function(){
    $('#sidenav').toggleClass("sideshow");
    $('#logo').toggle();
    $('#open-nav').toggle();
    $('.mainx').toggle();
});

